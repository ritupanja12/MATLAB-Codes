% MATLAB program to check whether the input character is vowel or consonant.

clc;
clear all;
close all;
ch = input('Enter the Character : ');
if ch == 'a';
    disp('It is a Vowel')
elseif ch == 'e';
    disp('It is a Vowel')
elseif ch == 'i';
    disp('It is a Vowel')
elseif ch == 'o';
    disp('It is a Vowel')
elseif ch == 'u';
    disp('It is a Vowel')
else
    disp('It is a Consonant')
end